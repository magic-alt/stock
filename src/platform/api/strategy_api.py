"""
Strategy management API endpoints (V5.0-A-3 backend).

Provides REST endpoints for strategy listing, validation, template generation,
and on-demand backtest execution via the platform APIRouter.
"""
from __future__ import annotations

import ast
import textwrap
import traceback
from typing import Any, Dict, List, Optional

from src.platform.api.router import APIRouter, RequestContext, ResponseContext
from src.core.logger import get_logger

logger = get_logger("platform.api.strategy")

strategy_router = APIRouter()


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _get_registry() -> Dict[str, Any]:
    """Lazy-load strategy registry to avoid circular imports."""
    from src.backtest.strategy_modules import STRATEGY_REGISTRY
    return STRATEGY_REGISTRY


def _strategy_summary(name: str, module: Any) -> Dict[str, Any]:
    """Summarise a strategy module for the API response."""
    params = {}
    if hasattr(module, "param_defs"):
        for pd_item in module.param_defs:
            params[pd_item.name] = {
                "type": pd_item.type.__name__ if hasattr(pd_item, "type") else "float",
                "default": pd_item.default if hasattr(pd_item, "default") else None,
            }
    elif hasattr(module, "coerce"):
        # Fallback: extract from coerce defaults
        try:
            defaults = module.coerce({})
            params = {k: {"type": type(v).__name__, "default": v} for k, v in defaults.items()}
        except Exception:
            pass
    return {
        "name": name,
        "description": getattr(module, "description", ""),
        "params": params,
    }


