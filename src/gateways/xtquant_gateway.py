"""
XtQuant/QMT Gateway - 讯投QMT实盘交易网关

Implements live trading connection via XtQuant SDK (QMT/MiniQMT).
This is the highest priority gateway for individual traders.

V3.2.0: Initial release

Features:
- Connection to QMT/MiniQMT terminal
- Order submission (limit, market)
- Order cancellation
- Account/Position queries
- Trade/Order callback handling
- Auto-reconnection

Dependencies:
- xtquant (provided by broker/QMT terminal)
- Running QMT/MiniQMT terminal with API enabled

Usage:
    >>> from src.gateways import XtQuantGateway, GatewayConfig
    >>> from queue import Queue
    >>> 
    >>> config = GatewayConfig(
    ...     account_id="YOUR_ACCOUNT",
    ...     broker="xtquant",
    ...     terminal_type="QMT",  # or "MiniQMT"
    ... )
    >>> 
    >>> event_queue = Queue()
    >>> gateway = XtQuantGateway(config, event_queue)
    >>> 
    >>> # Connect
    >>> gateway.connect()
    >>> 
    >>> # Send order
    >>> order_id = gateway.send_order(
    ...     symbol="600519.SH",
    ...     side="buy",
    ...     quantity=100,
    ...     price=1800.0,
    ... )
    >>> 
    >>> # Query account
    >>> account = gateway.query_account()
    >>> 
    >>> # Disconnect
    >>> gateway.disconnect()

References:
- BigQuant QMT教程: https://bigquant.com/wiki/doc/Ux2t1gwmNn
- ThinkTrader文档: https://dict.thinktrader.net/nativeApi/
- XtQuant API文档 (provided with terminal)
"""
