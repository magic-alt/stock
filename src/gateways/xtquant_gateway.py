if XTQUANT_AVAILABLE:
    
    class _XtQuantCallback(XtQuantTraderCallback):
        """
        Internal callback handler for xtquant events.
        
        Delegates all callbacks to XtQuantGateway methods.
        """
        
        def __init__(self, gateway: XtQuantGateway):
            super().__init__()
            self.gateway = gateway
        
        def on_disconnected(self):
            """Handle disconnect event."""
            self.gateway._process_disconnect_callback()
        
        def on_stock_order(self, order):
            """Handle order update."""
            self.gateway._process_order_callback(order)
        
        def on_stock_trade(self, trade):
            """Handle trade update."""
            self.gateway._process_trade_callback(trade)
        
        def on_order_error(self, order_id, error_id, error_msg):
            """Handle order error."""
            self.gateway.log.error(
                "xtquant_order_error_callback",
                order_id=order_id,
                error_id=error_id,
                error_msg=error_msg,
            )
        
        def on_cancel_error(self, order_id, error_id, error_msg):
            """Handle cancel error."""
            self.gateway.log.error(
                "xtquant_cancel_error_callback",
                order_id=order_id,
                error_id=error_id,
                error_msg=error_msg,
            )
        
        def on_order_stock_async_response(self, response):
            """Handle async order response."""
            self.gateway.log.debug(
                "xtquant_async_response",
                response=str(response),
            )
        
        def on_account_status(self, status):
            """Handle account status change."""
            self.gateway.log.info(
                "xtquant_account_status",
                status=str(status),
            )

else:
    _XtQuantCallback = None


