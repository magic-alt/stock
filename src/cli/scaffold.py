_TEMPLATE_PARAMS = {
    "trend_following": {"fast_period": 10, "slow_period": 30, "threshold": 0.01},
    "mean_reversion": {"lookback": 20, "entry_zscore": 2.0, "exit_zscore": 0.5},
    "ml_factor": {"n_estimators": 100, "lookback": 60, "retrain_freq": 20},
    "momentum": {"period": 20, "top_n": 5},
    "breakout": {"period": 20, "atr_mult": 2.0},
    "fundamental": {"metric": "pe_ratio", "threshold": 15.0},
}


def _to_class_name(snake: str) -> str:
    return "".join(w.capitalize() for w in snake.split("_"))


# ---------------------------------------------------------------------------
# Generator
# ---------------------------------------------------------------------------

class ScaffoldGenerator:
    """Code scaffold generator."""

    def __init__(self, base_dir: Optional[str] = None):
        self.base_dir = Path(base_dir) if base_dir else Path.cwd()

    def generate_strategy(
        self,
        name: str,
        template: str = "trend_following",
        output_dir: Optional[str] = None,
    ) -> dict:
        """Generate a strategy module + test file.

        Returns:
            Dict with paths of generated files.
        """
        class_name = _to_class_name(name)
        module_name = name.lower().replace("-", "_")
        params = _TEMPLATE_PARAMS.get(template, {"param1": 0})
        param_doc = "\n        ".join(f"{k}: {type(v).__name__} (default={v})" for k, v in params.items())

        strat_dir = Path(output_dir) if output_dir else self.base_dir / "src" / "strategies"
        test_dir = strat_dir.parent / "tests" if output_dir else self.base_dir / "tests"
        strat_dir.mkdir(parents=True, exist_ok=True)
        test_dir.mkdir(parents=True, exist_ok=True)

        strat_path = strat_dir / f"{module_name}.py"
        test_path = test_dir / f"test_{module_name}.py"

        strat_content = _STRATEGY_TEMPLATE.format(
            name=name,
            class_name=class_name,
            template=template,
            date=datetime.now().strftime("%Y-%m-%d"),
            default_params=repr(params),
            param_doc=param_doc,
        )

        test_content = _STRATEGY_TEST_TEMPLATE.format(
            name=name,
            class_name=class_name,
            module_name=module_name,
        )

        strat_path.write_text(strat_content, encoding="utf-8")
        test_path.write_text(test_content, encoding="utf-8")

        return {
            "strategy": str(strat_path),
            "test": str(test_path),
        }

    def generate_factor(
        self,
        name: str,
        output_dir: Optional[str] = None,
    ) -> dict:
        """Generate a factor module.

        Returns:
            Dict with paths of generated files.
        """
        class_name = _to_class_name(name)
        module_name = name.lower().replace("-", "_")

        factor_dir = Path(output_dir) if output_dir else self.base_dir / "src" / "pipeline"
        factor_dir.mkdir(parents=True, exist_ok=True)

        factor_path = factor_dir / f"{module_name}.py"
        content = _FACTOR_TEMPLATE.format(
            name=name,
            class_name=class_name,
            date=datetime.now().strftime("%Y-%m-%d"),
        )
        factor_path.write_text(content, encoding="utf-8")
        return {"factor": str(factor_path)}

    def list_templates(self) -> list:
        """List available scaffold templates."""
        return list(_TEMPLATE_PARAMS.keys())


# ---------------------------------------------------------------------------
# Click CLI
