if HAS_CLICK:

    @click.group()
    def scaffold():
        """Code scaffold generator."""
        pass

    @scaffold.command("strategy")
    @click.argument("name")
    @click.option("--template", "-t", default="trend_following",
                  type=click.Choice(list(_TEMPLATE_PARAMS.keys())),
                  help="Template type")
    @click.option("--output-dir", "-o", default=None, help="Output directory")
    def gen_strategy(name, template, output_dir):
        """Generate a new strategy scaffold."""
        gen = ScaffoldGenerator()
        result = gen.generate_strategy(name, template, output_dir)
        click.echo(f"Generated strategy: {result['strategy']}")
        click.echo(f"Generated test:     {result['test']}")

    @scaffold.command("factor")
    @click.argument("name")
    @click.option("--output-dir", "-o", default=None, help="Output directory")
    def gen_factor(name, output_dir):
        """Generate a new factor scaffold."""
        gen = ScaffoldGenerator()
        result = gen.generate_factor(name, output_dir)
        click.echo(f"Generated factor: {result['factor']}")

    @scaffold.command("templates")
    def list_templates():
        """List available templates."""
        gen = ScaffoldGenerator()
        for name in gen.list_templates():
            params = _TEMPLATE_PARAMS[name]
            click.echo(f"  {name:20s}  params: {params}")


